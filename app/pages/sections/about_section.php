        <h3>History</h3>
        <p class="">
           In 1969 The Ministry of Agriculture established a Natural Resources College (NRC) to provide certificate-level training for technical assistance in the field of agriculture for the Ministry.
        </p>
        <p>
          The College was built in 1956 on land belonged to Colby College. In 1979 the Government of Malawi got assistance from the Government of Canada through the Canadian International Develoment Agency (CIDA) to develop the NRC. The first enrolment of the student under NRC was in 1983. In 1999 the Malawi Government directed that NRC should be turned into a semi-autonomous institution. On 8th August 2001, the NRC was registered as a Public Trust under the Trustee Incorporation Act of 1962.
        </p>
        <p>
          Under the new legal framework, NRC broadened its training spectrum to embrace agricultural, environmental and natural resources management. On 14th December 2014, NRC integrated into Lilongwe University of Agriculture and Natural Resources (LUANAR) following dissolution of the Pulic Trust and became the second university campus.
        </p>  
